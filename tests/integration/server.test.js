test('failed artifact revisions retain inspectable source and fingerprints', async (t) => {
  const server = createServer({ fixtureMode: true });
  await new Promise((resolve) => server.listen(0, '127.0.0.1', resolve));
  t.after(() => server.close());
  const base = `http://127.0.0.1:${server.address().port}`;
  const spin = await request(base, '/api/spin', { method: 'POST', body: JSON.stringify({ seed: 'inspect-revisions', requestId: 'inspect-revisions-spin' }) });
  const ids = spin.body.selectedApis.map((entry) => entry.id);
  const concept = await request(base, `/api/runs/${spin.body.runId}/concept`, { method: 'POST', body: JSON.stringify({ requestId: 'inspect-revisions-concept', appName: 'Inspectable Failure', premise: 'A deliberately inspectable failed collision specimen for review.', playerAction: 'Press the review button to inspect each failed source revision.', noveltyDelta: 'The failure remains a readable artifact, not a blank page.', apiIds: ids, apiRoles: spin.body.selectedApis.map((entry) => ({ apiId: entry.id, essentialRole: `${entry.name} supplies an inspectable failure signal.`, operations: entry.operations.map((operation) => operation.id) })), causalChain: spin.body.selectedApis.map((entry, index) => ({ order: index + 1, apiId: entry.id, action: `turn ${entry.name} into an inspectable failure rule` })), dependency: { fromApiId: ids[0], to: 'rules', toApiId: ids[1], explanation: 'The first source changes the next failure rule.' }, interaction: { controls: ['review'], outcome: 'The reviewer sees a changing failed revision.' }, visualDirection: { style: 'maximalist review theatre', palette: 'black and cyan', typography: 'oversized serif', motion: 'revision cards turn over' }, bannedShapeAssessment: { plainDashboard: false, plainSearch: false, plainQuiz: false, randomFactDisplay: false, thinClone: false, plausibleStartupPitch: false, explanation: 'This is a failure review, not a product dashboard.' } }) });
  assert.equal(concept.response.status, 200);
  const first = '<!doctype html><html><head></head><body>first failed source</body></html>';
  const firstFailure = await request(base, `/api/runs/${spin.body.runId}/artifact`, { method: 'POST', body: JSON.stringify({ requestId: 'inspect-revisions-artifact', html: first }) });
  assert.equal(firstFailure.response.status, 422);
  const second = '<!doctype html><html><head></head><body>second failed source</body></html>';
  const secondFailure = await request(base, `/api/runs/${spin.body.runId}/repair`, { method: 'POST', body: JSON.stringify({ requestId: 'inspect-revisions-repair', html: second }) });
  assert.equal(secondFailure.response.status, 422);
  const rev1 = await fetch(`${base}/api/creations/${firstFailure.body.creationId}/source?revision=1`);
  const rev2 = await fetch(`${base}/api/creations/${firstFailure.body.creationId}/source?revision=2`);
  assert.match(await rev1.text(), /first failed source/);
  assert.match(await rev2.text(), /second failed source/);
  assert.equal(secondFailure.body.revisions[0].bytes, Buffer.byteLength(first));
  assert.equal(secondFailure.body.revisions[1].bytes, Buffer.byteLength(second));
  assert.match(secondFailure.body.revisions[0].sha256, /^[a-f0-9]{64}$/);
  assert.match(secondFailure.body.revisions[1].sha256, /^[a-f0-9]{64}$/);
});
