test('server refuses to execute a preserved pre-runtime-contract artifact', async (t) => {
  const store = new RunStore();
  const run = store.createRun({ requestId: 'server-early-runtime', selectedApis: [{ apiId: 'open-meteo', operationIds: ['forecast'] }] });
  store.acceptConcept(run.id, { requestId: 'server-early-concept', appName: 'Early Weather Clerk', apiIds: ['open-meteo'] });
  store.acceptArtifact(run.id, { requestId: 'server-early-artifact', html: '<script>window.earlyArtifactExecuted=true</script>', bytes: 50 });
  run.revisions[0].at = RUNTIME_CONTRACT_CUTOFF_MS - 1;
  const server = createServer({ fixtureMode: true, store });
  await new Promise((resolve) => server.listen(0, '127.0.0.1', resolve));
  t.after(() => server.close());

  const response = await fetch(`http://127.0.0.1:${server.address().port}/run/${run.creationId}`);
  const html = await response.text();
  assert.equal(response.status, 200);
  assert.match(html, /Runtime retired/);
  assert.match(html, /pre-contract specimen is not executed/);
  assert.doesNotMatch(html, /window\.randomware|earlyArtifactExecuted/);
  assert.equal(run.lastCapabilityExpiresAt, undefined);
});

test('MCP surface exposes eight annotated tools', async (t) => {
  const server = createServer({ fixtureMode: true });
  await new Promise((resolve) => server.listen(0, '127.0.0.1', resolve));
  t.after(() => server.close());
  const base = `http://127.0.0.1:${server.address().port}`;
  const initialize = await request(base, '/mcp', { method: 'POST', body: JSON.stringify({ jsonrpc: '2.0', id: 1, method: 'initialize', params: { protocolVersion: '2025-06-18', capabilities: {}, clientInfo: { name: 'integration', version: '1' } } }) });
  assert.equal(initialize.response.status, 200);
  assert.equal(initialize.body.result.protocolVersion, '2025-06-18');
  const ready = await request(base, '/mcp', { method: 'POST', body: JSON.stringify({ jsonrpc: '2.0', method: 'notifications/initialized' }) });
  assert.equal(ready.response.status, 202);
  const resources = await request(base, '/mcp', { method: 'POST', body: JSON.stringify({ jsonrpc: '2.0', id: 2, method: 'resources/list' }) });
  assert.equal(resources.body.result.resources[0].uri, 'ui://widget/randomware.html');
  const template = await request(base, '/mcp', { method: 'POST', body: JSON.stringify({ jsonrpc: '2.0', id: 3, method: 'resources/read', params: { uri: 'ui://widget/randomware.html' } }) });
  assert.equal(template.body.result.contents[0].mimeType, 'text/html;profile=mcp-app');
  const result = await request(base, '/mcp', { method: 'POST', body: JSON.stringify({ jsonrpc: '2.0', id: 4, method: 'tools/list' }) });
  assert.equal(result.response.status, 200);
  assert.equal(result.body.result.tools.length, 8);
  assert.ok(result.body.result.tools.every((tool) => tool.description.startsWith('Use this')));
  assert.ok(result.body.result.tools.every((tool) => tool.annotations && typeof tool.annotations.readOnlyHint === 'boolean'));
  const annotations = Object.fromEntries(result.body.result.tools.map((tool) => [tool.name, tool.annotations]));
  for (const name of ['open_randomware', 'spin_apis', 'get_run', 'mutate_creation', 'record_choreography_failure']) assert.equal(annotations[name].openWorldHint, false);
  for (const name of ['submit_concept', 'submit_artifact', 'submit_repair']) assert.equal(annotations[name].openWorldHint, true);
  const open = result.body.result.tools.find((tool) => tool.name === 'open_randomware');
  assert.equal(open._meta.ui.resourceUri, 'ui://widget/randomware.html');
  const openCall = await request(base, '/mcp', { method: 'POST', body: JSON.stringify({ jsonrpc: '2.0', id: 5, method: 'tools/call', params: { name: 'open_randomware', arguments: {} } }) });
  assert.deepEqual(openCall.body.result.content, [{ type: 'text', text: 'Randomware slot mounted.' }]);
  assert.equal(openCall.body.result.structuredContent.ok, true);
  const get = await fetch(`${base}/mcp`);
  assert.equal(get.status, 405);
});

