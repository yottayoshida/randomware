const fs = require('node:fs');
const path = require('node:path');
const { responseContract } = require('../src/core/response-contract');

const root = path.resolve(__dirname, '..');
const directory = path.join(root, 'docs/api-candidates/adapted');
const contracts = {};
for (const filename of fs.readdirSync(directory).filter((name) => name.endsWith('.json')).sort()) {
  const fixture = JSON.parse(fs.readFileSync(path.join(directory, filename), 'utf8'));
  if (!fixture.apiId || !fixture.operationId || !Object.prototype.hasOwnProperty.call(fixture, 'data')) throw new Error(`invalid_adapted_golden:${filename}`);
  contracts[filename] = responseContract(fixture.data);
}
const target = path.join(root, 'src/core/response-contracts.generated.js');
const source = `// Generated by scripts/generate-response-contracts.js from owner-curated adapted goldens.\nmodule.exports = Object.freeze(${JSON.stringify(contracts, null, 2)});\n`;
fs.writeFileSync(target, source);
console.log(`response contracts generated (${Object.keys(contracts).length})`);
